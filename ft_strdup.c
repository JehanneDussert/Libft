#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int i;
	char *s2;

	while (s1[i])
		i++;
	if (!(s2 = malloc(sizeof(s1)*i+1)))
		return(NULL);
	i = -1;
	while (s1[++i])
		s2[i] = s1[i];
	s2[i] = '\0';
	return(s2);
}
