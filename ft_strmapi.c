#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*dst;
	int		i;

	i = 0;
	if (s && f)
	{
		if (!(dst = (char *)malloc(sizeof(char) * ft_strlen((char *)s) + 1)))
			return (NULL);
	}
	else
		return (NULL);
	while (s[i])
	{
		dst[i] = f(i, s[i]);
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
