#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t			i;
	unsigned char	*dest;
	unsigned char	*source;

	i = 0;
	dest = (unsigned char *)dst;
	source = (unsigned char *)src;
	while (i < n)
	{
		*dest = *source;
		if (*source == (unsigned char)c)
			return (++dest);
		dest++;
		source++;
		i++;
	}
	return (NULL);
}
